x = [3 1 5 7 9 2 6];

%a 
x(3) % 3rd element

%b 
x(1:7) % 1st to 7th elements of x 

%c
x(1:end) % 1st to last elements of x

%d
x(1:end-1) % 1st to last but one elements of x

%e
x(6:-2:1) % 6th element to 1st element with steps of 2 in x  

%f
x([1 6 2 1 1]) % the respective elements of x

%g
sum(x) %sum of elements in x 

